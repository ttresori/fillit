#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	void	*r;
	size_t	i;

	i = 0;
	r = s;
	while (i < n)
	{
		*(unsigned char*)s = c;
		s++;
		i++;
	}
	return (r);
}
